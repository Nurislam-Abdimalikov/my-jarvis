"""Тесты XTTS-нормализатора.

Покрывают типичные ответы Джарвиса: температура, проценты, время, числа,
эмодзи, единицы скорости. Если что-то снова поломает произношение топонимов
("Бищкэк") или цифры — этот тест сразу красный.
"""

from __future__ import annotations

import pytest
from jarvis.tts._text_normalize import normalize_for_tts


@pytest.mark.parametrize(
    "raw,expected",
    [
        # Температура и единицы (главный кейс из жалобы юзера).
        (
            "В Бишкеке 22°C, ветер 5.2 км/ч.",
            "В Бишкеке двадцать два градусов, ветер пять целых две десятых километров в час.",
        ),
        # Проценты.
        ("Громкость 100%, сэр.", "Громкость сто процентов, сэр."),
        # Время.
        ("Сейчас 18:45.", "Сейчас восемнадцать сорок пять."),
        # Иностранное название не трогаем.
        ("Открыл Google Chrome", "Открыл Google Chrome"),
        # Эмодзи срезаем.
        ("Готово, сэр. 🎉", "Готово, сэр."),
        # Отрицательные числа.
        ("-5°C на улице.", "минус пять градусов на улице."),
        # Единицы времени только сразу после числа, не внутри слова.
        ("Через 30 минут будет дождь.", "Через тридцать минут будет дождь."),
        ("Через 30 мин дождь.", "Через тридцать минут дождь."),
        ("У него минута на ответ.", "У него минута на ответ."),
    ],
)
def test_normalize_for_tts(raw: str, expected: str) -> None:
    assert normalize_for_tts(raw) == expected
